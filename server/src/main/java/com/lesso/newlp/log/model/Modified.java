public class Modified {
    String name;
    String originalVal;
    String modifiedVal;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getOriginalVal() {
        return originalVal;
    }

    public void setOriginalVal(String originalVal) {
        this.originalVal = originalVal;
    }

    public String getModifiedVal() {
        return modifiedVal;
    }

    public void setModifiedVal(String modifiedVal) {
        this.modifiedVal = modifiedVal;
    }
}
